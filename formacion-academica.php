                      <p class="txt-General" >El Instituto de Derechos Humanos y Desarrollo (“IDHDES”) de la Universidad de San Martín de Porres, creado con la finalidad de propiciar espacios abiertos de reflexión y dialogo a partir de la publicación de trabajos de investigación, entrevistas especializadas, difusión de cursos presenciales y virtuales, así como la realización de eventos académicos que permitan la capacitación académica y participación activa de la sociedad civil, llevará a cabo, en el marco de su Segundo Aniversario el evento “LIBERTAD DE EXPRESIÓN Y COMUNICACIÓN POLÍTICA EN LAS REDES SOCIALES”.
