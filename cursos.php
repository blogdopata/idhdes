        <h4 class="titleH4"> SOBRE EL CONCURSO</h4>
        <p class="txt-General">El Concurso de Fotografía “EL ROSTRO DE MIS DERECHOS”, promovido el Instituto de Derechos Humanos y Desarrollo de la Universidad de San Martín de Porres – IDHDES USMP,  busca que los participantes desde una perspectiva social mediante el arte del dibujo puedan plasmar un momento con el cual se pueda denunciar, difundir y sensibilizar respeto de la situación de los derechos humanos en nuestro país.
