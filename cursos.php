2.  Creatividad del dibujo presentado <br>
3.  Originalidad del trabajo  <br> 
4.  Ser alusiva al derecho a graficar. 
</p>



 <h4 class="titleH4">PRESENTACIÓN Y FORMATO  </h4> 

   <p class="txt-General">
1.  Cada participante podrá presentar sólo 1 (un) dibujo a color.  Sólo se tomarán en cuenta los sobres que contengan un solo dibujo.  <br>
2.  Cada Participante desarrollará la temática conforme a sus propios lineamientos estéticos y creativos. <br>
3.  Tamaño mínimo A4. <br>
4.  No se aceptarán reproducciones ni obras que muestren directamente leyendas o marcas que puedan interpretarse – a juicio del jurado – como publicidad. <br>
5.  Los dibujos no deben haber sido presentadas en ninguna exposición, muestra u otro similar al presente concurso. <br>
6.  Cada participante deberá presentar su dibujo adjuntando la ficha de datos personales.
</p>
 <h4 class="titleH4">PUBLICACIÓN DE RESULTADOS</h4>
  <p class="txt-General">
El lunes 10 de septiembre se publicarán los resultados del Concurso, anunciándose el nombre de los (as) ganadores del Concurso, mediante la plataforma web del IDHDES y/o redes sociales.
</p>
 <h4 class="titleH4">
INSCRIPCIÓN</h4>
