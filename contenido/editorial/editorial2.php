              <p  class="txt-General" >
             Existen, en la actualidad, distintas visiones respecto de la discapacidad. Una de ellas propugna que, lejos de ser una barrera u obstáculo, es una cualidad diferenciada, la cual no se presenta en el resto de personas.
</p>
 <p  class="txt-General" >
Esta es, al parecer, la idea de Robert Hensel, a quien se le atribuye la frase que ha servido de título para este breve artículo. Se parte de la premisa que la “discapacidad” es un concepto que se ha construido en el contexto de sociedades que establecen barreras para las personas que integran este colectivo, lo que les impide integrarse en igualdad de condiciones.
</p>
 <p  class="txt-General" >
De esta forma, se hace referencia a un modelo social de discapacidad, el cual busca resaltar que los defectos no están en la persona, sino en un entorno creado en función de un individuo “promedio”. Este nuevo entendimiento de la discapacidad tiene bastante relevancia para una adecuada comprensión del contenido y alcance de los derechos. De hecho, la Organización de las Naciones Unidas, al aprobar la Convención sobre los Derechos de las Personas con Discapacidad, tuvo en cuenta este modelo al indicar que la discapacidad es un concepto que “evoluciona y que resulta de la interacción entre las personas con deficiencias y las barreras debidas a la actitud y al entorno que evitan su participación plena y efectiva en la sociedad”.
</p>
 <p  class="txt-General" >
Evidentemente, la idea de “participación” conlleva, naturalmente, el reconocimiento de distintos aspectos vitales para la adecuada inclusión de las personas con discapacidad en el mapa social. Las reflexiones que, a propósito de esta idea de “capacidades diferenciadas” se pueden generar deben apuntar, fundamentalmente, a convencer a la sociedad y el Estado que los obstáculos y los impedimentos se han creado y trasladado a estos colectivos, y que, en buena cuenta, eso nos responsabiliza de los obstáculos impuestos.
