							La tempestad de los escándalos de Odebrecht perdura hasta hoy. Entre el sismo político de acusaciones mutuas y la fundada
							indignación de la sociedad peruana, se han trazado profundas zanjas entre la legitimidad del gobierno y la voluntad
							constituyente del pueblo peruano. No ha sido casualidad que la “Gobernabilidad democrática frente a la corrupción”
							haya sido el tema central de los compromisos tomados por los jefes de estado en la Octava Cumbre de las Américas.
							Es de notar en este documento los compromisos tomados en relación a la protección de los defensores de derechos humanos
							y los servidores públicos(1) . Sin embargo, la corrupción afecta un sinnúmero de derechos humanos.
						</p>
						<p class="txt-General">
							El vínculo entre la Corrupción y los derechos humanos ha sido de sobra reconocido tanto en el sistema universal como en el
							sistema interamericano. El prefacio de la Convención de las Naciones Unidas contra la Corrupción del 31 de octubre
							de 2013 a cargo del entonces secretario general Kofi Annan llamaba a la corrupción como una “plaga insidiosa que tiene
							un amplio espectro de consecuencias corrosivas para la sociedad. Socaba la democracia y el Estado de Derecho, da pie
							a violaciones de derechos humanos, (2) . No podríamos estar más convencidos que él.

						</p>

						<p class="txt-General">
							Desde el trabajo de la Relatora Christy Mbonu(3) hasta el Informe Final del Comité Asesor del Consejo de Derechos Humanos(4)
							la posición es clara: la corrupción genera un efecto perverso sobre un amplio espectro de derechos humanos. Ya sea
							de forma directa como ocurre con la micro corrupción en el Poder Judicial(5) , o indirecta, por la asignación ineficaz
							y desviación de los fondos del estado para obras públicas(6), las consecuencias ulteriores son la negación de los
							derechos civiles, políticos, económicos, sociales y culturales de los ciudadanos peruanos.
						</p>

						<p class="txt-General">
							Por su parte, el sistema interamericano en la Convención contra la Corrupción de 1996(7), comprensiblemente se centró en
							la erradicación de la corrupción en la función pública y en la adopción de medidas para la tipificación penal. En
							febrero de este año, la Comisión Interamericana presentó el Informe sobre la situación de los derechos humanos en
							Venezuela, en el que destaco que “El Estado tiene el deber de evitar que estos hechos de corrupción se repitan, por
							lo que resulta fundamental la adopción de medidas preventivas. (…) Ello es fundamental para el respeto de todos los
							derechos civiles, políticos, económicos, sociales, culturales y ambientales en Venezuela”(8) .
						</p>
						<p class="txt-General">
							Después de los eventos del siglo pasado, el orden internacional experimentó un proceso de institucionalización y humanización(9).
							Hoy en día, indudablemente el ser humano es un sujeto de derecho internacional protegido en casi todos los ámbitos
							de su vida. Debe quedar claro: El Estado peruano tiene a cargo obligaciones generales de respeto, garantía y adopción
							de medidas internas de los derechos humanos especialmente la Convención Americana de Derechos Humanos, El Pacto sobre
							los Derechos Civiles y Políticos, el Pacto sobre los derechos económicos, sociales y culturales y el núcleo duro de
							las normas ius cogens(10).
						</p>

						<p class="txt-General">
							Frente al escenario actual de corrupción público - privada, la prevención y erradicación de la corrupción se convierte forzosamente
							en un tema de interés público. Consideramos que la gestión pública debe adoptar una política anticorrupción con un
							enfoque en derechos humanos. Dos aspectos deben ser desarrollados: La responsabilidad social empresarial y las políticas
							públicas enfocadas en derechos humanos como prevención.
						</p>
						<p class="txt-General">
							Finalmente, la cooperación internacional y el conocimiento cabal de las obligaciones internacionales del Estado peruano,
							así como la concientización de la sociedad peruana sobre los efectos nocivos de la corrupción se constituye como un
							objetivo en el cual los Centros de Investigación, como el que elabora el presente trabajo, deben desarrollar una ardua
							labor de investigación académica y difusión.
						</p>



