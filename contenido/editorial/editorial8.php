       Las redes sociales se han convertido en un medio efectivo de comunicación y de expresión de ideas, pensamientos y opiniones, que permite acortar distancias, acceder en tiempo real a información ilimitada y difundir todo tipo de conocimiento, permitiendo conectarse a todo el mundo y posibilitando el dialogo directo con instituciones, personalidades artísticas o políticas, antes, inaccesibles para el común de la gente.
     </p>
<p  class="txt-General" >
Sin embargo, a pesar de su innegable utilidad, redes como twitter o facebook también puede ser una vía presta para un ejercicio abusivo y arbitrario de la libertad de expresión que conlleve a la violación de derechos fundamentales, como el honor, buena reputación o a la intimidad, cuya vulneración afecta directamente la dignidad humana, bien jurídico inherente a la persona y de especial protección constitucional. 
</p>
<p  class="txt-General" >
Es así que nuestra Constitución dispone que todos los peruanos- civiles y funcionarios públicos- debemos respetarla, cumplirla y defenderla así como al ordenamiento jurídico, entendiéndose que el ejercicio de nuestras libertades y derechos observan un infranqueable límite: ahí donde comienza el derecho de los demás.
</p>
<p  class="txt-General" >
En el caso de los funcionarios públicos, el ejercicio de su libertad de expresión, sin importar el medio que utilice para tal fin, observa ciertas connotaciones especiales, que no solo abarca limites como el deber de confidencialidad de cierta información vinculada a su función o de denunciar violaciones de derechos, sino también aquellos inherentes a la naturaleza de su función relacionados con la ética y el interés público, considerando que los funcionarios públicos están al servicio de la nación y sus actos deben resultar efectivos y favorables a la realización de los derechos de la persona. En consecuencia, el empleo que hagan de las redes sociales y medios virtuales exige un especial cuidado, no solo por la naturaleza de sus funciones sino por la alta accesibilidad y concurrencia del medio social.
</p>
<p  class="txt-General" >
Cabe resaltar que un servidor público debe ceñirse a los elementos esenciales de la democracia representativa como son el respeto de los derechos humanos y libertades y; el ejercicio del poder con sujeción al Estado de Derecho, tal como lo establece la Carta Democrática. En esa línea la Corte Interamericana de Derechos Humanos en el caso Apitz Barbera y otros ha establecido que los funcionarios públicos están sometidos "a ciertas limitaciones en cuanto a constatar en forma razonable, aunque no necesariamente exhaustiva, los hechos en los que fundamentan sus opiniones, y deberían hacerlo con una diligencia aún mayor a la empleada por los particulares, en atención al alto grado de credibilidad de la que gozan y en aras a evitar que los ciudadanos reciban una versión manipulada de los hechos" entendiéndose que por su alta investidura resultan garantes del ejercicio efectivo de los derechos fundamentales.
</p>
<p  class="txt-General" >
